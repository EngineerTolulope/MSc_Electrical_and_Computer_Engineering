    private void privatekeyMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_privatekeyMouseClicked
        // TODO add your handling code here:
        while (true) {
            x = (int) (Math.random() * p);
            if (x != 0) {
                break;
            }
        }
        privatekey.setText(x + "");
    }//GEN-LAST:event_privatekeyMouseClicked

    private void randMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_randMouseClicked
        // TODO add your handling code here:
        while (true) {
            r = (int) (Math.random() * p);
            if (r != 0) {
                break;
            }
        }
        rand.setText(r + "");
    }//GEN-LAST:event_randMouseClicked

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        BigInteger M = BigInteger.valueOf(m).mod(BigInteger.valueOf(p));
        BigInteger yR = BigInteger.valueOf(y).modPow(BigInteger.valueOf(r), BigInteger.valueOf(p));

        c2 = (M.multiply(yR)).mod(BigInteger.valueOf(p));
       
        c2com.setText(c2 +"");
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        int tmpx = p-1-x;
        BigInteger tmp, message;
        tmp = c1.pow(tmpx);
        message = (tmp.multiply(c2)).mod(BigInteger.valueOf(p));
        
        decrypt.setText(message + "");
        
    }//GEN-LAST:event_jButton4ActionPerformed

